private Horario setDataToHr(Horario hr){
        String hrInicio, hrTermino;
        hr.setIdHorario(Integer.parseInt(this.txtIdHorario.getText()));
        hrInicio = String.valueOf(this.cmbHoraInicio.getSelectedItem()) +":"+ String.valueOf(this.cmbMinutoInicio.getSelectedItem()) + ":" + "00";
        hrTermino = String.valueOf(this.cmbHoraTermino.getSelectedItem()) +":"+ String.valueOf(this.cmbMinutoTermino.getSelectedItem()) + ":" + "00";
        hr.setHoraInicio(hrInicio);
        hr.setHoraTermino(hrTermino);
        return hr;
    }
private void loadDataToTableHour()throws SQLException{
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/SCA", "root", "");
    Statement stm = con.createStatement();  
    String query = "SELECT * FROM HORARIOS";
    ResultSet rs = stm.executeQuery(query);
    ResultSetMetaData rsMeta = rs.getMetaData();
    
    int col = rsMeta.getColumnCount();
    DefaultTableModel modeloTabla = new DefaultTableModel();
    this.hTable.setModel(modeloTabla);
    
    for(int x = 1; x < col+1; x++){
        modeloTabla.addColumn(rsMeta.getColumnLabel(x));
    }
    
    while(rs.next()){
        Object[] fila = new Object[col];
        
        for(int y = 0; y < col; y++){
            fila[y] = rs.getObject(y+1);
        }
        modeloTabla.addRow(fila);
    }
}
