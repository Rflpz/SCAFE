    private void addItemsComoboxHorario(){
        this.cmbHoraInicio.removeAllItems();
        this.cmbHoraTermino.removeAllItems();
        this.cmbMinutoInicio.removeAllItems();
        this.cmbMinutoTermino.removeAllItems();
        
        for(int i = 7; i < 22; i++){
            if(i < 10){
                this.cmbHoraInicio.addItem("0"+i);
                this.cmbHoraTermino.addItem("0"+i);
            }
            else{
                this.cmbHoraInicio.addItem(i);
                this.cmbHoraTermino.addItem(i);
            }
        }
        for(int i = 0; i< 60; i=i+10){
            if(i < 10){
                this.cmbMinutoInicio.addItem("0"+i);
                this.cmbMinutoTermino.addItem("0"+i);
            }
            else{
                this.cmbMinutoInicio.addItem(i);
                this.cmbMinutoTermino.addItem(i);
            }
        }
    }
