    private void setInfoViewFromGrp(Grupo grp){
        this.txtIdGrupo.setText(Integer.toString(grp.getIdGrupo()));
        this.txtNombreGrupo.setText(grp.getNombre());
        int selCmbCarrera = 0;
        switch(grp.getCarrera()){
            case "INGENIERIA DE SOFTWARE":{
                selCmbCarrera = 0;
                break;
            }
            case "INGENIERIA EN TELEMATICA":{
                selCmbCarrera = 1;
                break;
            }
            default:
                break;
        }
        this.cmbCarrera.setSelectedIndex(selCmbCarrera);
        
        int selCmbGrado = 0;
        switch(grp.getGrado()){
            case "1":{
                selCmbCarrera = 0;
                break;
            }
            case "2":{
                selCmbCarrera = 1;
                break;
            }
            case "3":{
                selCmbCarrera = 2;
                break;
            }
            case "4":{
                selCmbCarrera = 3;
                break;
            }
            case "5":{
                selCmbCarrera = 4;
                break;
            }
            case "6":{
                selCmbCarrera = 5;
                break;
            }
            case "7":{
                selCmbCarrera = 6;
                break;
            }
            case "8":{
                selCmbCarrera = 7;
                break;
            }
            default:
                break;
        }
        this.cmbGrado.setSelectedIndex(selCmbCarrera);
    }
